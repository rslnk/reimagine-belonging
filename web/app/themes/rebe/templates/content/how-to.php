			<h2 id="faq" class="o-heading c-how-to-faq__title">Frequently Asked Questions</h2>

		</div>

		<dl class="_o-list c-how-to-faq__list">

			<dt class="c-how-to-faq__item c-how-to-faq__question">
				Can I use this in my classroom or community?
			</dt>
				<dd class="c-how-to-faq__item c-how-to-faq__answer">
					Yes! Please use this website. We will continue to release materials
					such as curriculum and literature guides to assist you in doing this,
					so sign up for periodic updates. If you would like support from our
					team, check out our <a href="/workshops">workshop and screening</a>
					offerings.
				</dd>

			<dt class="c-how-to-faq__item c-how-to-faq__question">
				Can I share my story or experience on this website?
			</dt>
				<dd class="c-how-to-faq__item c-how-to-faq__answer">
					Almost. We are currently building the functionality that will allow
					visitors to add experiences to the <a href="/events">Event</a> section
					and a forthcoming Visions section. If you would like to submit a video
					for our <a href="http://reimaginebelonging.org/stories">Stories</a>
					collection, please <a href="mailto:info@withwingsandroots.com">email us</a>
					directly.
				</dd>

			<dt class="c-how-to-faq__item c-how-to-faq__question">
				Why are Germany and the US the focus on this website?
			</dt>
				<dd class="c-how-to-faq__item c-how-to-faq__answer">
					The U.S.A. remains the world’s largest immigration destination, while
					Germany is largest in Europe and third worldwide. Berlin and New York
					were chosen as emblematic cities of global migration to allow audiences
					to explore questions of migration and identity in their own society
					with a transnational lens. However, immigration is not an U.S. or
					German “problem” — it is a global reality. With time, we hope to
					co-produce historical timelines and story collections with partners in
					additional locations.
				</dd>

			<dt class="c-how-to-faq__item c-how-to-faq__question">
				Who are the people who were interviewed for this site?
			</dt>
				<dd class="c-how-to-faq__item c-how-to-faq__answer">
					The criteria for selecting interviewees were simple: the person must
					live in New York or Berlin and have either immigrated themselves at a
					young age or have been born to immigrant parents. We were committed to
					collect stories from various communities, experiences and perspectives.
				</dd>

			<dt class="c-how-to-faq__item c-how-to-faq__question">
				How did you find the interviewees?
			</dt>
				<dd class="c-how-to-faq__item c-how-to-faq__answer">
					In our initial efforts were between 2005-2008, during which time we
					interviewed over 50 people. We received recommendations from various
					community organizations, as well as through the team’s personal
					networks. We continue to collect interviews in our
					<a href="/workshops">workshops</a> and add them to this site.
				</dd>

			<dt class="c-how-to-faq__item c-how-to-faq__question">
				Who created this website?
			</dt>
				<dd class="c-how-to-faq__item c-how-to-faq__answer">
					Reimagine Belonging was created by the initiative Wings and Roots, a
					team of independent educators, media makers and scholars working for
					immigrant rights and racial justice. We produce media and educational
					tools to challenge rigid and exclusionary debates around the topics of
					migration, integration and identity. The team is based in various
					European and US cities and hail from many backgrounds — racial, n
					ational, religious, and linguistic. The project was initiated by
					Christina Antonakos-Wallace in 2005.
				</dd>

		</dl>

