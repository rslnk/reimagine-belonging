			<dt class="c-how-to-faq__item c-how-to-faq__question">
				What is the relationship between reimaginebelonging.org and the film
				“With Wings and Roots”?
			</dt>
				<dd class="c-how-to-faq__item c-how-to-faq__answer">
					<stong>reimaginebelonging.org</stong> is part of the same overall
					initiative: Wings and Roots. Both this website and the initiative grew
					out of the documentary film With Wings and Roots. The film tells the
					stories of four young people from immigrant families who work for
					social inclusion. The site and film explore similar topics and were
					produced by many of the same of people, but use different forms.
					To learn more about the film, visit
					<a href="http://withwingsandrootsfilm.com" target="_blank">withwingsandrootsfilm.com</a>
				</dd>

